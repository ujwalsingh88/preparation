package com.java.preparation.model;

public class Java8Model {

	private int id;
	private int age;
	private int salary;
	private String name;
	
	
	
	public Java8Model(int id, int age, int salary, String name) {
		super();
		this.id=id;
		this.age=age;
		this.salary=salary;
		this.name=name;
	}
	
	public int getId() {
		return id;
	}



	public int getAge() {
		return age;
	}



	public int getSalary() {
		return salary;
	}



	public String getName() {
		return name;
	}



}
